#include "3-calc.h"

/**
 * op_add - add
 * description: add
 * @a: a
 * @b: b
 * Return: 0
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub
 * description: sub
 * @a: a
 * @b: b
 * Return: 0
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - mul
 * description: mul
 * @a: a
 * @b: b
 * Return: 0
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - div
 * description: div
 * @a: a
 * @b: b
 * Return: 0
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod
 * description: mod
 * @a: a
 * @b: b
 * Return: 0
 */

int op_mod(int a, int b)
{
	return (a % b);
}
